export default function SocialFloat({ links }: { links: SocialLink[] }) {
  const visible = links.filter(l => l.active && l.url);
  if (!visible.length) return null;

  return (
    <div className="fixed bottom-6 right-6 z-50 flex flex-col-reverse gap-3">
      {visible.map(({ platform, url }) => {
        const s = STYLES[platform];
        if (!s) return null;
        return (
          <a key={platform} href={url} target="_blank" rel="noopener noreferrer"
            aria-label={`ติดต่อผ่าน ${s.label}`}
            className="group flex items-center justify-center w-13 h-13 rounded-full relative"
            style={{ width: 52, height: 52, background: s.bg, boxShadow: `0 6px 24px ${s.shadow}` }}>
            {ICONS[platform]}
            <span className="absolute inset-0 rounded-full animate-ping opacity-20"
              style={{ background: s.ping }} />
            {/* Tooltip */}
            <span className="absolute right-full mr-3 whitespace-nowrap text-xs font-medium text-white bg-black/70 px-2 py-1 rounded-lg opacity-0 group-hover:opacity-100 transition-opacity pointer-events-none">
              {s.label}
            </span>
          </a>
        );
      })}
    </div>
  );
}
